passwordRegex = r"^(?=.*?[A-Z])(?=.*?[a-z])(?=.*?[0-9])(?=.*?[#?!@$%^&*-]).{8,}$"
nameRegex = r"/^[a-zA-ZàáâäãåąčćęèéêëėįìíîïłńòóôöõøùúûüųūÿýżźñçčšžÀÁÂÄÃÅĄĆČĖĘÈÉÊËÌÍÎÏĮŁŃÒÓÔÖÕØÙÚÛÜŲŪŸÝŻŹÑßÇŒÆČŠŽ∂ð ,.'-]+$/u"


def checkEmail(email):
    if re.fullmatch(emailRegex, email):
        return True
    else:
        return False

def checkPassword(password1, password2):
    if re.fullmatch(passwordRegex, password1) and password1 == password2:
        return True
    else:
        return False

def checkName(name):
    if len(name) > 0:
        return True
    elif re.fullmatch(nameRegex, name):
        return True
    else:
        return False
